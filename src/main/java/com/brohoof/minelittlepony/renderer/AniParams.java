package com.brohoof.minelittlepony.renderer;

public class AniParams {

    public float move;
    public float swing;
    public float tick;
    public float horz;
    public float vert;

    public AniParams(float move, float swing, float tick, float horz, float vert) {
        this.move = move;
        this.swing = swing;
        this.tick = tick;
        this.horz = horz;
        this.vert = vert;
    }
}
